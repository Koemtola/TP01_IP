<?php
echo "Exampe 03 : Switch statement  <br><br> ";
$myFavColor = 'pink';
switch ($myFavColor) {
    case 'pink':
        echo "My favorite car color is pink!";
        break;
    case 'red':
        echo "My favorite car color is red!";
        break;
    case 'orange':

        echo "My favorite car color is orange!";
        break;
    default:
        echo "My favorite car color is not pink, red , or orange!";
}
?>
